// ให้ Declare ฟังก์ชัน add
function add(a, b) {
    return a + b;
};
// ทดสอบ ฟังก์ชัน add
console.log(add(5, 3));  // แสดงผล: 8



// ให้ Declare ฟังก์ชัน add
function subtract(a, b) {
    return a - b;
};
// ทดสอบ ฟังก์ชัน subtract
console.log(subtract(10, 5));  // แสดงผล: 5



// ให้ Declare ฟังก์ชัน multiply
function multiply(a, b) {
    return a * b;
};
// ทดสอบ ฟังก์ชัน subtract
console.log(multiply(10, 5));  // แสดงผล: 50



// ให้ Declare ฟังก์ชัน divide
function divide(a, b) {
    return a / b;
};
// ทดสอบ ฟังก์ชัน divide
console.log(divide(10, 5));  // แสดงผล: 2